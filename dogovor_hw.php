	<h2>Условия  соглашения по оказанию возмездных услуг между Заказчиком и Исполнителем</h2>

	<h4>1. ПРЕДМЕТ СОГЛАШЕНИЯ</h4>
	<p class="text">
		<span class="digits">1.1.</span>
		По настоящему Соглашению Заказчик поручает, а Исполнитель принимает на себя обязанности по оказанию возмездных услуг квалифицированной помощи (сопровождению) в подготовке и написании работ (далее Заказ) в объеме, оговоренном в Заявке, а Заказчик принимает на себя обязанности по оплате оказанных услуг в размере и на условиях, оговоренных в Заявке.
