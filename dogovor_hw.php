	<ul class="no-marked">
		<li>2.1.1. Четко сформулировать требования к Заказу и предоставить Исполнителю материалы, необходимые для исполнения Заказа (методические рекомендации, задания, образцы и т.д.).
		</li>
		<li>2.1.2. В соответствии с условиями Соглашения в полном объеме и в установленные сроки оплатить  услуги, предусмотренные настоящим Соглашением.
		</li>
		<li>2.1.3. Получить в оговоренный срок все выполненный Заказ. 
		</li>
		<li>2.1.4. Предоставить все необходимые материалы (данные, образцы), утвердить выполненные этапы работы, согласовать необходимые процедуры и ответить на возможные возникшие вопросы по Заказу в четко оговоренные кратчайшие сроки.</li>
	</ul>
	<h5>2.2. Права Заказчика:</h5>
	<ul class="no-marked">
		<li>2.2.1. Внести в течение 2-х рабочих дней с момента оформления Заказа уточнения и дополнения к Заказу, не меняющие объем оказываемых услуг. (В случае внесения изменений и дополнений по истечении 2-дневного срока Исполнитель вправе изменить стоимость Заказа по согласованию сторон, а Заказчик обязуется оплатить услуги).
