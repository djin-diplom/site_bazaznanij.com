<h1><a href="articles.php">Полезные статьи</a> → Как правильно написать введение к  дипломной работе. Образец</h1>На первый взгляд может показаться, что раздел введение в дипломном проекте написать просто, потому что он занимает примерно 2-4 страницы А4. Однако, именно в этом разделе сконцентрирована вся суть работы. Для написания введения к дипломной работе необходимо составить его план.<br><br>
В этом разделе нужно ответить на следующие вопросы по дипломной работе:
<ol type="1" start="1">
<li>Четко определяем актуальности дипломного проекта;</li>
<li>Выстроенная логическая цепочка с задачей, целями и их разрешением. Это своего рода план этапов проведения исследований; </li>
<li>Выделить объект и предмет анализ дипломной работы;</li>
<li>Краткий анализ источников, которые были использованы в работе;</li>
<li>Способы и методы решения задач, которые ставит перед собой тема диплома.</li>
</ol>
Следовательно, введение заключает в себе главные этапы выполнения работы, а также задачи, которые будут освещаться в дипломе. В любой дипломной работе во введении нужно выделять следующие элементы:
<ol type="1" start="1">
<li>Обозначить актуальность тематики дипломной работы;</li>
<li>Современность проблемы и показать кто из ученых занимался данной проблемой;</li>
<li>Выделить цель дипломной работы и задачи для ее решения;</li>
<li>Объект и предмет анализа дипломного проекта;</li>
<li>Практическая значимость выводов дипломной работы на практике;</li>
<li>Структурная расстановка содержания диплома.</li>
</ol>
Рассмотрим на примере. Допустим, готовится дипломная работа по праву, тема «Проектирование технологии управления персоналом на предприятии здравоохранения». Первое, что необходимо сделать, это четко сформулировать актуальность темы. «Актуальность темы проектирование технологии управления персоналом на предприятии здравоохранения вызвана необходимостью внедрять новые формы и методы управления трудовым коллективом и за счет этого достигать более высоких финансовых результатов».<br><br>
Следующий этап, это обозначение цели исследований. Цель работы – определить перечень мероприятий, направленных на совершенствование технологии управления персоналом персонала ООО «Уратино» (медицинский центр). Достижению поставленной цели способствует решение следующих задач:
<ul type="disc">
<li>изучить современную концепцию технологии управления персоналом;</li>
<li>рассмотреть элементы и формы мотивации в системе управления персоналом;</li>
<li>выявить особенности технологии управления персоналом в сфере здравоохранения;</li>
<li>охарактеризовать систему управления и процессы технологии управления персоналом в ООО «Уратино»;</li>
<li>выявить проблемы и ограничения действующей технологии управления персоналом и наметить пути их устранения;</li>
<li>предложить рекомендации, направленные решение выявленных проблем и расчет экономической эффективности.  </li>
<li>Объектом исследования выступает коммерческая организация сферы здравоохранения ООО «Уратино» г. Москва. Предмет – система управления персоналом, ее особенности, формы и элементы, а так же проблемы и ограничения. </li>
</ul>
Рассматриваемые вопросы не являются принципиально новыми. Анализ теоретический аспектов технологии управления персоналом представлен в работах М.И. Булдакова, Б.М. Генкина и других исследователей.<br><br>  Необходимость совершенствования управления медицинским персоналом обозначена в работах А.С. Алейникова, И.Н. Антоновой и других.  Особенности технологии управления персоналом сотрудников медицинских учреждений, в том числе и стоматологических клиник, рассмотрена такими авторами, как Л.И.Герасимова, Р.С. Матвеев, В.Н. Викторов, С.Ю. Иванов, М.А. Пигусов и другими. Разработке предложений, направленных на повышение эффективности организации и нормирования труда, посвящены работы Е.Н. Мазуровой, О.С. Симанковой и других практиков технологии управления персоналом.<br><br>
Зачастую в  дипломной работе применяют сопоставительный метод и метод правового анализа. В нашем примере, методологическую базу работы составил системный подход, включающий комплексный анализ финансовых, организационных, социально-психологических аспектов технологии управления персоналом. Использовались такие методы, как наблюдение, описание, анализ документов.<br><br>
