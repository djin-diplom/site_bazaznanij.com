<h1><a href="articles.php">Полезные статьи</a> → Материал для дипломной работы и его систематизация</h1>Ресурсы, которые вы используете для своего дипломного проекта, могут быть любые – интернет, электронные источники, учебная литература, статьи, материалы из производственных практик. Это могут быть любые источники способные вам помочь.<br><br>
В таком изобилии информации очень трудно запутаться, поэтому именно этап сбора литературы сам по себе является самым напряженным. Поэтому, собирая материал, нужно очень внимательно следить за появляющимися данными.<br>
<br>
Чтобы не растеряться в мире знаний, нужно суметь правильно систематизировать данные.  При этом, информацию с разных источников, нужно не объединять, а распределять по отдельным папкам. Вся ненужная информация потом удалиться, а вы будете знать, откуда что взялось.<br>
<br>
Создайте на рабочем столе специальную папку и наполняйте ее информацией, которая необходима для работы. На конечном этапе сбора информации – это папка очень поможет вам писать диплом только из нужных данных.<br>
<br>
Отбирать нужную информацию – это тоже довольно трудоемкий процесс. Надеемся, у вас есть готовый план дипломной работы, а если нет – обратитесь к своему руководителю. Выбирайте пункты плана и подбирайте на них материал. Стоит понимать, что информация должна строго соответствовать пункту, раскрывать суть и отвечать на поставленные вопросы. Порядок и правильное упорядочение информации – отличная оценка и рецензия на диплом.<br>
<br>
Но в любом систематизировании могут случиться пробелы. Студент, пишущий диплом, устает, теряет внимательность – это человеческий фактор. Мы уже писали, что информация с разных источников, не должна находиться в одной папке – это делается для библиографического списка. Человек, проверяющий вашу работу, должен видеть, что информацию вы не придумали сами, а взяли с достоверных источников. При соблюдении этих правил вам будет намного легче вносить правки в готовый диплом, а ошибки будут не такими критичными.<br>
<br>
Никто вам не говорит, что писать диплом можно только с первых пунктов, не пропуская ни одного. Это лично ваше дело, начните хоть с последнего, но запоминайте каждый свой шаг, чтобы в распечатанном дипломе не оказалось нежелательных пробелов. Также, обратите внимание на подразделы, они требуют такого же внимания, как и общие пункты.<br>
<br>
Итак, вы занялись дипломной работой. Выяснив все вышеперечисленные требования, представьте мысленно, каким должен быть диплом. Вы можете посмотреть любые другие дипломы, их оформление, пункты, и уже отталкиваясь от этого создавать свою индивидуальную работу.<br>
<br>
